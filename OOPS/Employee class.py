class Employee:
    def details(self,employeename,id,department,salary):
        self.employeename=employeename
        self.id=id
        self.department=department
        self.salary=salary
    def printdetails(self):
        print(self.employeename,self.id,self.department,self.salary)
ee=Employee()
ee.details("viky",1,"bpo",12000)
ee.printdetails()

